package lessons.lesson5.constructorExample;


public class Cat {
    String name;
    String color;
    int age;
    boolean isHungry;

    public Cat(String name, String color, int age, boolean isHungry) {
        this.name = name;
        this.color = color;
        this.age = age;
        this.isHungry = isHungry;
    }
}
